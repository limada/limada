    public abstract class FontMeasure {
        public FontMeasure() {
            Make();
        }

        private IDictionary<char, Size> table = new Dictionary<char, Size>();
        public string Name { get; protected set; }
        public abstract void Make ();
        public void Add (long c, double width, double height) {
            table.Add(char.ConvertFromUtf32((int)c)[0], new Size(width, height));
        }
        public Size Measure(char c) {
            if (table.ContainsKey(c))
                return table[c];
            else
                return Size.Zero;
        }
    }

